import math
a = float(input('Digite um anglo desejado: '))
print('O seno desse angulo é igual á: {:.2f}'.format(math.sin(math.radians(a))))
print('O cosseno dessse angulo é igual á: {:.2f}'.format(math.cos(math.radians(a))))
print('A tangente desse angulo é igual á: {:.2f}'.format(math.tan(math.radians(a))))